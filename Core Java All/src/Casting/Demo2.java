package Casting;

public class Demo2
{
	public static void main(String [] args)
	{
		double a=66.8;
		System.out.println(a);
		int b=(int)a;
		System.out.println(b);
		int m=68;
		System.out.println(m);
		char n=(char)m;
		System.out.println(n);
		float x=21.5f;
		System.out.println(x);
		int y=(int)x;
		System.out.println(y);
	}
}
/*
OUTPUT:-
66.8
66
68
D
21.5
21
*/